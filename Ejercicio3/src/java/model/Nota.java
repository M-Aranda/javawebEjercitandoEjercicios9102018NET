package model;

/**
 *
 * @author Marce
 */
public class Nota {
    
    private int id;
    private int valor;

    public Nota() {
    }

    public Nota(int id, int valor) {
        this.id = id;
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getValor() {
        return valor;
    }

    public void setValor(int valor) {
        this.valor = valor;
    }
    
    
    
    
    
}
